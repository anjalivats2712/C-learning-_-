// an arithmetic operation btw 
// int and int is int
// int and float is float 
// float and float is float

// results of following calculations are imp
#include <stdio.h>

int main(){
    printf("%d\n",5/2);
    printf("%f\n",5.0/2);
    printf("%d\n",2/5);
    printf("%f\n",2.0/5);
    return 0;


}
// 5/2=2
// 2/5=0
// 5.0/2=2.5
// 2.0/5=.4